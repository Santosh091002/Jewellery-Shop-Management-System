    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
       String name = txtVendorName.getText();
    String phoneNumber = txtVendorPhoneNumber.getText();
    String jname = txtjname.getText();
    String jdescription = txtjdescription.getText();
    String jquantity = txtjquantity.getText();
    String jPrice = txtjprice.getText();
    String jewelleryId = generateUniqueJewelleryId();
    
    if (name.isEmpty() || phoneNumber.isEmpty() || jname.isEmpty() || jPrice.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all required fields.");
        return;
    }
    
    boolean jewelleryAlreadyPurchased = false;
    DefaultTableModel jewelleryModel = (DefaultTableModel) tablejewellerysupplied.getModel();
    for (int i = 0; i < jewelleryModel.getRowCount(); i++) {
        String existingJName = (String) jewelleryModel.getValueAt(i, 3);
        if (jname.equalsIgnoreCase(existingJName)) {
            jewelleryAlreadyPurchased = true;
            break;
        }
    }
    
    if (jewelleryAlreadyPurchased) {
        JOptionPane.showMessageDialog(this, "Jewellery item already purchased.");
    } else {
        try (Connection con = ConnectionProvider.getCon()) {
            PreparedStatement ps = con.prepareStatement("INSERT INTO jewellerysuplied (jewsup_pk, v_name, v_phonenumber, name, description, quantity, price) VALUES (?, ?, ?, ?, ?, ?, ?)");

            ps.setString(1, jewelleryId);
            ps.setString(2, name);
            ps.setString(3, phoneNumber);
            ps.setString(4, jname);
            ps.setString(5, jdescription);
            ps.setString(6, jquantity);
            ps.setString(7, jPrice);

            ps.executeUpdate();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error inserting data into the database: " + e.getMessage());
            e.printStackTrace(); // Print the stack trace for debugging purposes
            return;
        }

        jewelleryModel.addRow(new Object[]{jewelleryId, name, phoneNumber, jname, jdescription, jquantity, jPrice});
        jewelleryModel.fireTableDataChanged();
    }  
           
    }//GEN-LAST:event_btnAddActionPerformed

