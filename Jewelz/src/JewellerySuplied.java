    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         int rowIndex = tablejewellerysupplied.getSelectedRow();
        if (rowIndex == -1) {
            JOptionPane.showMessageDialog(null, "Please select a record to delete.");
            return;
        }
        String jewelleryId = tablejewellerysupplied.getValueAt(rowIndex, 0).toString();
        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("DELETE FROM jewellerysuplied WHERE jewsup_pk = ?");
            ps.setString(1, jewelleryId);
            int deletedRows = ps.executeUpdate();
            if (deletedRows > 0) {
                JOptionPane.showMessageDialog(null, "JewellerySupply Details deleted successfully.");
           
                DefaultTableModel model = (DefaultTableModel) tablejewellerysupplied.getModel();
                model.removeRow(rowIndex);
            } else {
                JOptionPane.showMessageDialog(null, "Failed to delete employee.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

