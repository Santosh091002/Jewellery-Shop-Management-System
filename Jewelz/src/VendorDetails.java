    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int rowIndex = tablevendor.getSelectedRow();
    if (rowIndex == -1) {
        JOptionPane.showMessageDialog(null, "Please select a record to delete.");
        return;
    }
    String vendorID = tablevendor.getValueAt(rowIndex, 0).toString();
    try {
        Connection con = ConnectionProvider.getCon();
        PreparedStatement ps = con.prepareStatement("DELETE FROM vendor WHERE vendor_pk = ?");
        ps.setString(1, vendorID);
        int deletedRows = ps.executeUpdate();
        if (deletedRows > 0) {
            JOptionPane.showMessageDialog(null, "vendor deleted successfully.");
           
            DefaultTableModel model = (DefaultTableModel) tablevendor.getModel();
            model.removeRow(rowIndex);
        } else {
            JOptionPane.showMessageDialog(null, "Failed to delete vendor.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

