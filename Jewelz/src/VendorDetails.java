    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tablevendor.getModel();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from vendor");
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("vendor_pk"),rs.getString("name"), rs.getString("address"), rs.getString("phonenumber")});
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
        btnUpdate.setEnabled(false);
    }//GEN-LAST:event_formComponentShown

