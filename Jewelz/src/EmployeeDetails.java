    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        
        String name = txtName.getText();
        String email = txtEmail.getText();
        String phonenumber = txtPhonenumber.getText();
        String salary = txtSalary.getText();

        if (validateFields()) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("insert into employee(name,email,phonenumber,salary) values(?,?,?,?)");
                
                ps.setString(1, name);
                ps.setString(2, email);
                ps.setString(3, phonenumber);
                ps.setString(4, salary);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Employee Added successfully");
                setVisible(false);
                new EmployeeDetails().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
                                           
    }//GEN-LAST:event_btnsaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        String name = txtName.getText();
        String email = txtEmail.getText();
        String phonenumber = txtPhonenumber.getText();
        String salary = txtSalary.getText();

        if (validateFields()) {
            JOptionPane.showMessageDialog(null, "All fields are required");
        } else {
            try {
                Connection con = ConnectionProvider.getCon();
                PreparedStatement ps = con.prepareStatement("update employee SET name=?,email=?,phonenumber=?,salary=? where employee_pk =?");
                ps.setString(1, name);
                ps.setString(2, email);
                ps.setString(3, phonenumber);
                ps.setString(4, salary);
                ps.setInt(5, employeePk);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Employee Updated successfully");
                setVisible(false);
                new EmployeeDetails().setVisible(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

