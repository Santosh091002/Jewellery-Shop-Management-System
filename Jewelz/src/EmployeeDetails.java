    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         int rowIndex = tableemployee.getSelectedRow();
    if (rowIndex == -1) {
        JOptionPane.showMessageDialog(null, "Please select a record to delete.");
        return;
    }
    String employeeID = tableemployee.getValueAt(rowIndex, 0).toString();
    try {
        Connection con = ConnectionProvider.getCon();
        PreparedStatement ps = con.prepareStatement("DELETE FROM employee WHERE employee_pk = ?");
        ps.setString(1, employeeID);
        int deletedRows = ps.executeUpdate();
        if (deletedRows > 0) {
            JOptionPane.showMessageDialog(null, "Employee deleted successfully.");
       
            DefaultTableModel model = (DefaultTableModel) tableemployee.getModel();
            model.removeRow(rowIndex);

            // Decrement IDs in the table
            decrementIDs(rowIndex);
        } else {
            JOptionPane.showMessageDialog(null, "Failed to delete employee.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}

private void decrementIDs(int deletedIndex) {
    DefaultTableModel model = (DefaultTableModel) tableemployee.getModel();
    for (int i = deletedIndex; i < model.getRowCount(); i++) {
        int id = Integer.parseInt(model.getValueAt(i, 0).toString());
        model.setValueAt(id - 1, i, 0); // Decrementing the ID
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

